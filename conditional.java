public class conditional {
    public static void main(String[]args) {
        int a = 90;
        int b = 10;

        //if
        if(a == b){
            System.out.println("a is equal to b");
        }
        
       // else if
       else if(a != b ) {
            System.out.println("a is not equal to b ");
        }

        //else
        else {
            System.out.println("a is neither equal to b nor uneqal to b");
        }
    }
}